inline bool fruit_exists()
{
    using namespace std::chrono;
    return fruit_appear_timer.get_time_elapsed_ms() < 10s && !fruit_collected && has_game_started;
}

inline FRUIT get_level_fruit(int level)
{
    return level_fruit[std::clamp(level, 0, (int)level_fruit.size() - 1)];
}

inline void set_music(Mix_Music &music)
{
    if (current_mus == &music)
        return;

    Mix_PlayMusic(&music, -1);
    current_mus = &music;
}

inline float get_maze_off_y()
{
    return TTF_FontHeight(font) * 2;
}

inline bool is_fright_mode()
{
    return fright_timer.get_time_elapsed_ms() < FRIGHT_PERIOD && has_game_started;
}

inline int map_at(int y, int x)
{
    if (y < 0 || y >= ROWS_N)
        return OUT_OF_BOUND;
    if (x < 0 || x >= COLS_N)
        return OUT_OF_BOUND;

    return map[y][x];
}